                const newClubPayload: Omit<Club, 'id'> = {
                    name: clubName,
                    ownerId: currentUser.id,
                    whatsappConfig,
                };
                const newClub = await createClub(newClubPayload);
                
                // Promote selected player to admin for that club
                const playerToPromote = players.find(p => p.id === adminId);
